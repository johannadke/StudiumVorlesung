package ModerneProgrammierkonzepte.Reflection;

public class Person {
    public String name;
    public int alter;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAlter() {
        return alter;
    }

    public void setAlter(int alter) {
        this.alter = alter;
    }
}
